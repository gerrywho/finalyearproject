$(function () {
    var pars = ["3", "4", "5"];
    var sel = document.getElementById('par1');
    for (var i = 0; i < pars.length; i++) {
        var opt = document.createElement('option');
        opt.innerHTML = pars[i];
        opt.value = pars[i];
        sel.appendChild(opt);
    }
});

